public class ControlFlowExercises {
    public static void main(String[] args) {
        //1c For
        //
        //Alter your loop to count backwards by 5's from 100 to -10.

        int i;
        for (i = 100; i >= -10; i--) {
            System.out.println(i + " ");
            i = i -4;
        }
    }
}
